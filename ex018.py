import math
ang = float(input('Digite o valor do ângulo: '))
sen = math.sin(ang)
cos = math.cos(ang)
tan = math.tan(ang)
print('''O seno vale: {:.1f}
O cosseno vale: {:.1f}
A tangente vale: {:.1f}'''.format(sen, cos, tan))


